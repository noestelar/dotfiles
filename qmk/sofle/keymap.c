// Encoder: Left = Volume, Right = Scroll (overrides kb default)
#ifdef ENCODER_ENABLE
bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { // Left encoder
        if (get_highest_layer(layer_state) == _LOWER) {
            // Undo/Redo on LOWER layer
            tap_code16(clockwise ? C(KC_Y) : C(KC_Z));
        } else {
            // Volume
            tap_code(clockwise ? KC_VOLU : KC_VOLD);
        }
    } else if (index == 1) { // Right encoder
        if (get_highest_layer(layer_state) == _RAISE) {
            // Horizontal scroll on RAISE
            tap_code(clockwise ? KC_RIGHT : KC_LEFT);
        } else {
            // Vertical scroll
            tap_code(clockwise ? KC_PGDN : KC_PGUP);
        }
    }
    return false;
}
#endif
