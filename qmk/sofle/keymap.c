static void render_miku_anim(void) {
    static uint32_t anim_timer = 0;
    static uint8_t current_frame = 0;

    if (timer_elapsed32(anim_timer) > 500) { // Switch frame every 500ms
        anim_timer = timer_read32();
        current_frame = (current_frame + 1) % 2;
    }

    oled_set_cursor(0, 0);
    if (current_frame == 0) {
        oled_write_raw_P(miku_frame1, sizeof(miku_frame1));
    } else {
        oled_write_raw_P(miku_frame2, sizeof(miku_frame2));
    }
}

// Master side: layer + status info (vertical 32px wide)
static void render_status(void) {
    oled_write_P(PSTR("\n"), false);

    // Layer name
    oled_write_P(PSTR("LAYER"), false);
    oled_write_P(PSTR("\n"), false);
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_P(PSTR("Base\n"), false);
            break;
        case _LOWER:
            oled_write_P(PSTR("Lower"), false);
            break;
        case _RAISE:
            oled_write_P(PSTR("Raise"), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("Adjst"), false);
            break;
        default:
            oled_write_P(PSTR("???  "), false);
    }

    oled_write_P(PSTR("\n\n"), false);

    // Modifier indicators
    uint8_t mods = get_mods();
    oled_write_P(PSTR("MODS\n"), false);
    oled_write_P((mods & MOD_MASK_SHIFT) ? PSTR("SFT ") : PSTR("    "), false);
    oled_write_P((mods & MOD_MASK_CTRL)  ? PSTR("CTL ") : PSTR("    "), false);
    oled_write_P((mods & MOD_MASK_ALT)   ? PSTR("ALT ") : PSTR("    "), false);
    oled_write_P((mods & MOD_MASK_GUI)   ? PSTR("CMD ") : PSTR("    "), false);

    oled_write_P(PSTR("\n\n"), false);

    // Caps lock
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.caps_lock ? PSTR("CAPS!") : PSTR("     "), false);

    oled_write_P(PSTR("\n\n"), false);
    oled_write_P(PSTR(" Noe "), false);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_270;
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status();
    } else {
        render_miku_anim();
    }
    return false; // We handled it, skip kb default
}

